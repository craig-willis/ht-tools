package edu.illinois.lis.ht.index.mpi;

public class MetsFile {

	String id;
	String seq;
	String href;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getHref() {
		return href;
	}
	public void setHref(String href) {
		this.href = href;
	}	
}
